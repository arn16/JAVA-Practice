public class Lecture5Code3 {
    public static void main(String[] args) {
        // to print the triangle
        /*
         
         * 
         * *
         * * *
         * * * *
            
         */

        for(int i=1;i<=4;i++){
            for(int j=1;j<=i;j++){
                System.out.print(" * ");
               
            } 
            System.out.print("\n");
        }

    }
    
}
